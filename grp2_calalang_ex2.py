#Task 1: Performs Arithmetic Operations
print("Task 1: Arithmetic Operations")

#Assigned two variables
a = 9
b = 4
print("a =",a)
print("b =",b)

#Addition Operation
result = a + b
#Display the result
print("Addition Operation:", f"{a} + {b} = {result}")

#Subtraction Operation
result = a - b
print("Subtraction Operation:", f"{a} - {b} = {result}")

#Division Operation
result = a / b
print("Division Operation:", f"{a} / {b} = {result}")

#Multiplication Operation
result = a * b
print("Multiplication Operation:", f"{a} * {b} = {result}")

#Modulus Operation
result = a % b
print("Modulus Operation:", f"{a} % {b} = {result}")

#Exponential Operation
result = a ** b
print("Exponential Operation:", f"{a} ** {b} = {result}")

